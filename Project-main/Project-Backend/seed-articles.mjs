async function seed() {
  const conn = await mysql.createConnection(config)
  try {
    const [existing] = await conn.execute('SELECT COUNT(*) as cnt FROM articles')
    if (existing[0].cnt > 0) {
      console.log(`มีบทความอยู่แล้ว ${existing[0].cnt} บทความ ข้าม seed`)
      return
    }
    for (const art of articles) {
      await conn.execute(
        'INSERT INTO articles (title, description, image, content) VALUES (?, ?, ?, ?)',
        [art.title, art.description, art.image, art.content]
      )
    }
    console.log(`✅ Seed สำเร็จ เพิ่ม ${articles.length} บทความ`)
  } finally {
    await conn.end()
  }
}

seed().catch(console.error)
